# 출력 함수 : print()

#하나만 출력합니다.
print("#하나만 출력합니다")
print("Hello Python Programming")

#한줄 띄기
print()  #Hello python programming뒤 한줄이 떨어짐

print("Python \n") #\n을 사용하면 print()와 같다

#여러 개를 출력합니다.
